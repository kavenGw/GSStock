def _get_or_create_category(name, parent_id=None):
    """返回 (category, created)，已存在则复用"""
    from app.models.category import Category
    from app import db

    existing = Category.query.filter_by(name=name, parent_id=parent_id).first()
    if existing:
        return existing, False

    category = Category(name=name, parent_id=parent_id)
    db.session.add(category)
    db.session.commit()
    return category, True


def seed_ascend_category():
    """幂等 seed：昇腾超节点产业链一级+二级板块、股票、归属、投资建议。

    设计原则：
    - 已存在的分类/股票/归属**一律不覆盖**
    - 只补齐缺失项
    - 跨板块股票（002916/601138/688041 已属 CPU 板块）不在本列表中，由 StockCategory 唯一约束兜底
    - 失败不抛出，只记录日志（启动不可被 seed 阻断）
    """
    from app import db
    from app.models.stock import Stock
    from app.models.category import Category, StockCategory

    try:
        parent, parent_created = _get_or_create_category(ASCEND_PARENT, parent_id=None)
        if parent_created:
            logger.info(f'[seed.ascend] 创建一级板块 {ASCEND_PARENT} id={parent.id}')

        sub_map = {}
        for sub_name in SUBCATEGORIES:
            sub, created = _get_or_create_category(sub_name, parent_id=parent.id)
            sub_map[sub_name] = sub.id
            if created:
                logger.info(f'[seed.ascend] 创建二级板块 {sub_name} id={sub.id}')

        added_stock = 0
        added_category = 0
        added_advice = 0
        skipped_category = 0

        for item in ASCEND_STOCKS:
            code = item['code']
            name = item['name']
            sub_name = item['sub']
            advice = item['advice']
            sub_id = sub_map.get(sub_name)
            if sub_id is None:
                logger.warning(f'[seed.ascend] 子分类 {sub_name} 查找失败，跳过 {code}')
                continue

            stock = Stock.query.get(code)
            if not stock:
                stock = Stock(stock_code=code, stock_name=name)
                db.session.add(stock)
                db.session.flush()
                added_stock += 1

            sc = StockCategory.query.filter_by(stock_code=code).first()
            if not sc:
                db.session.add(StockCategory(stock_code=code, category_id=sub_id))
                added_category += 1
            elif sc.category_id != sub_id:
                skipped_category += 1
                logger.debug(f'[seed.ascend] {code} 已归属 category_id={sc.category_id}，保留不动')

            if not (stock.investment_advice or '').strip():
                stock.investment_advice = advice
                added_advice += 1

        db.session.commit()

        if added_stock or added_category or added_advice or parent_created:
            logger.info(
                f'[seed.ascend] 完成 新增股票={added_stock} 新增归属={added_category} '
                f'写入建议={added_advice} 保留已有归属={skipped_category}'
            )
    except Exception as exc:
        db.session.rollback()
        logger.warning(f'[seed.ascend] 执行失败（忽略，不阻断启动）: {exc}')
